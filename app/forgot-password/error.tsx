"use client";

import { useEffect } from "react";

export default function Error({
  error,
  reset,
}: {
  error: Error & { digest?: string };
  reset: () => void;
}) {
  useEffect(() => {
    console.error("Forgot password page error:", error);
  }, [error]);

  return (
    <div className="min-h-screen flex items-center justify-center p-4">
      <div className="text-center">
        <h2 className="text-2xl font-bold text-gray-900 mb-4">
          Something went wrong
        </h2>
        <p className="text-gray-600 mb-6">
          We encountered an error loading the password reset page.
        </p>
        <button
          onClick={reset}
          className="px-6 py-3 bg-primary text-black font-bold rounded-xl hover:bg-primary-dark transition-all"
        >
          Try again
        </button>
      </div>
    </div>
  );
}
